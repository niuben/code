// 判断是否是帧数
function isInt(num) {    
    if (Math.floor(num) == this) {
        return true;
    } else {
        return false;
    }
}
/* ---- */
isInt(123);

/* ---- */
// 判断是否是小数
function isFloat(num) {
    if (Math.floor(num) != this) {
        return true;
    } else {
        return false;
    }
}
/* ---- */
isFloat(12.2);
